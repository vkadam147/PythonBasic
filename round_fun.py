#The round() function is used to round a number to the nearest integer or to a specified number of decimal places
#round(number, digits)
#number → The value you want to round
#digits (optional) → Number of decimal places to keep. If not given, it rounds to the nearest whole number.
print(round(3.13,1))
print(round(7.666,2))
print(round(7.5))
print(round(6.5))